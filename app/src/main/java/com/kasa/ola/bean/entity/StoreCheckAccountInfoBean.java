package com.kasa.ola.bean.entity;

public class StoreCheckAccountInfoBean {

    private String income;
    private String verifiedMoney;
    private String noVerifiedMoney;
    private String verifiedNum;
    private String noVerifiedNum;

    public String getIncome() {
        return income;
    }

    public void setIncome(String income) {
        this.income = income;
    }

    public String getVerifiedMoney() {
        return verifiedMoney;
    }

    public void setVerifiedMoney(String verifiedMoney) {
        this.verifiedMoney = verifiedMoney;
    }

    public String getNoVerifiedMoney() {
        return noVerifiedMoney;
    }

    public void setNoVerifiedMoney(String noVerifiedMoney) {
        this.noVerifiedMoney = noVerifiedMoney;
    }

    public String getVerifiedNum() {
        return verifiedNum;
    }

    public void setVerifiedNum(String verifiedNum) {
        this.verifiedNum = verifiedNum;
    }

    public String getNoVerifiedNum() {
        return noVerifiedNum;
    }

    public void setNoVerifiedNum(String noVerifiedNum) {
        this.noVerifiedNum = noVerifiedNum;
    }
}
